<div>
<h2 style="font-size:16px;font-weight:600;color:#111;margin-bottom:10px;">5. Pagos y sistema de escrow</h2>
<p style="font-size:14px;color:#6b7280;line-height:1.7;">Los pagos se procesan de forma segura a través de Stripe. Una vez realizado el pago, los fondos quedan retenidos en escrow hasta que el comprador confirme la recepción del objeto. El pago se libera automáticamente al vendedor tras la confirmación de entrega o pasados 7 días desde el marcado de envío si el comprador no reporta ningún problema.</p>
</div>

<div>
<h2 style="font-size:16px;font-weight:600;color:#111;margin-bottom:10px;">6. Obligaciones del vendedor</h2>
<p style="font-size:14px;color:#6b7280;line-height:1.7;">El vendedor se compromete a: (a) describir los objetos con exactitud y sin omisiones relevantes; (b) enviar el objeto en un plazo máximo de 10 días naturales desde la confirmación del pago, con número de seguimiento válido; (c) hacerse responsable de los daños o pérdidas durante el envío hasta la entrega; (d) cumplir con sus obligaciones fiscales correspondientes. El incumplimiento del plazo de envío puede resultar en la cancelación automática de la venta y el reembolso íntegro al comprador.</p>
</div>

<div>
<h2 style="font-size:16px;font-weight:600;color:#111;margin-bottom:10px;">7. Protección al comprador</h2>
<p style="font-size:14px;color:#6b7280;line-height:1.7;">Si el objeto recibido no corresponde a la descripción, el comprador dispone de 3 días desde la recepción para abrir una disputa a través de la plataforma. RialBids actuará como mediador. Si se confirma que el objeto no es como fue descrito, el comprador podrá recibir un reembolso total o parcial. Si el vendedor no envía el objeto en el plazo establecido, la venta se cancelará y el comprador recibirá un reembolso completo.</p>
</div>

<div>
<h2 style="font-size:16px;font-weight:600;color:#111;margin-bottom:10px;">8. Comisiones y honorarios</h2>
<p style="font-size:14px;color:#6b7280;line-height:1.7;">RialBids cobra una comisión del 9% más €3 sobre el precio final de cada venta exitosa. Esta comisión se descuenta automáticamente del monto transferido al vendedor. Los gastos de envío corren por cuenta del comprador salvo indicación contraria en el lote.</p>
</div>

